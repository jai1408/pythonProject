# Dynamic Typing
'''Python uses dynamic typing, meaning you can reassign variables to different data types.
This makes Python very flexible in assigning data types;
it differs from other languages that are statically typed.'''
my_dogs = 2
print(my_dogs)
print(type(my_dogs))
my_dogs = ['Sammy', 'Oscar']
print(my_dogs)
print(type(my_dogs))

